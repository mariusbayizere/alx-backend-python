#!/usr/bin/env python3
"""
module is responsible to  concatenate a String
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.

    Args:
        str1 (str): The first number.
        str2 (str): The second number.

    Returns:
        str: The concatenation of str1 and str2.
    """
    return str1 + str2
